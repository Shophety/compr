    private void agregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_agregarMouseClicked
        if (nomProd.getText().isEmpty() || precProd.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ERROR: Campo(s) vacio(s)");
        } else if (!Texto.validar(nomProd.getText())) {
            JOptionPane.showMessageDialog(this, "ERROR: Nombre solo letras Y numeros permitidos");
        } else if (nomProd.getText().length() > 15) {
            JOptionPane.showMessageDialog(this, "ERROR: Nombre maximo 15 caracteres");
        } else {
            try {
                Float prec = Float.parseFloat(precProd.getText());
                if (prec < 0) {
                    JOptionPane.showMessageDialog(this, "ERROR: Campo precio solo positivo permitido");
                } else {
                    DecimalFormatSymbols s = new DecimalFormatSymbols();
                    s.setDecimalSeparator('.');
                    DecimalFormat f = new DecimalFormat("#.00", s);
                    if (!Usuario.agregarProducto(user, nomProd.getText(), f.format(prec))) {
                        JOptionPane.showMessageDialog(this, "ERROR: Ya existe este producto");
                    } else {
                        JOptionPane.showMessageDialog(this, "PRODUCTO AGREGADO CON EXITO");
                        nomProd.setText("");
                        precProd.setText("");
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "ERROR: Campo precio solo decimal permitido");
            }
        }


    }//GEN-LAST:event_agregarMouseClicked

