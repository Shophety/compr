    private void eliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_eliminarMouseClicked
        // TODO add your handling code here:
        
        
                
        if (lista.getSelectedIndex() == -1) {
            JOptionPane.showMessageDialog(this, "ERROR: Ningun usuario seleccionado");
        } else {
            if (String.valueOf(pass.getPassword()).isEmpty()) {
                JOptionPane.showMessageDialog(this, "ERROR: Campo contrase√±a vacio");
            } else {
                try {
                    if (!Usuario.eliminarUsuario(lista.getModel().getElementAt(lista.getSelectedIndex()),String.valueOf(pass.getPassword()))) {
                        JOptionPane.showMessageDialog(this, "ERROR: Contrase√±a incorrecta");
                    } else {
                        JOptionPane.showMessageDialog(this, "USUARIO ELIMINADO CON EXITO");
                        new login().setVisible(true);
                        dispose();
                    }
                } catch (IOException ex) {
                    Logger.getLogger(eliminarUsuario.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_eliminarMouseClicked

